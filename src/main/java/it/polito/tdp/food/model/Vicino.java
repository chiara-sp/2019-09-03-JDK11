package it.polito.tdp.food.model;

public class Vicino {

	String p;
	int peso;
	public Vicino(String p, int peso) {
		super();
		this.p = p;
		this.peso = peso;
	}
	public String getP() {
		return p;
	}
	public void setP(String p) {
		this.p = p;
	}
	public int getPeso() {
		return peso;
	}
	public void setPeso(int peso) {
		this.peso = peso;
	}
	
	
}
